function [route,numExpanded] = DijkstraGrid (input_map, start_coords, dest_coords)

cmap = [1 1 1; ...% 1 - white - clear cell
        0 0 0; ...% 2 - black - obstacle
        0 0 1; ...% 3 - red = visited
        1 1 0; ...% 4 - blue  - on list
        1 0 1; ...% 5 - green - start
        1 0 0; ...% 6 - yellow - destination
	    0 1 1];

colormap(cmap);

% variable to control if the map is being visualized on every
% iteration
drawMapEveryTime = true;

[nrows, ncols] = size(input_map);

% map - a table that keeps track of the state of each grid
% cell????????????,????????????????????????????????????
%nrows=10;ncols=10;
map = zeros(nrows,ncols);

map(~input_map) = 1;   % Mark free cells
map(input_map)  = 2;   % Mark obstacle cells

% Generate linear indices of start and dest nodes
start_node = sub2ind(size(map), start_coords(1), start_coords(2));%???????????????map???????????????????????????
dest_node  = sub2ind(size(map), dest_coords(1),  dest_coords(2));%???????????????map???????????????????????????

map(start_node) = 3;%5-green
map(dest_node)  = 4;%6-yellow

% Initialize distance array
distanceFromStart = Inf(nrows,ncols);%????????????????????????????????????????????????????????????????????????.????????? distanceFromStart  ???????????????????????????????????? Inf??????????????????????????????
%????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????? Inf???


% For each grid cell this array holds the index of its parent(?????????
parent = zeros(nrows,ncols);%?????????????????????????????????

distanceFromStart(start_node) = 0;%?????????????????????????????????0

% keep track of number of nodes expanded 
numExpanded = 0;

% Main Loop
%???????????????????????????????????????????????????
while true
    
    % Draw current map
    map(start_node) = 5;
    map(dest_node) = 6;
    
    % make drawMapEveryTime = true if you want to see how the 
    % nodes are expanded on the grid. 
    if (drawMapEveryTime)
        image(1.5, 1.5, map);%image??????????????????????????????????????????????????????????????????????????????????????????
        grid on;%????????????
        axis image;%???????????????
        drawnow;
    end
    
    % Find the node with the minimum distance
    [min_dist, current] = min(distanceFromStart(:));%???????????????????????????:current,?????????????????????????????????:min_dist
    
    if ((current == dest_node) || isinf(min_dist))%??????????????????????????????????????????????????????????????????????????????????????????????????????
        break;
    end;
    
    % Update map
    map(current) = 3;         % mark current node as visited ??? map ???????????????????????? 3(??????) ????????????????????????????????????????????????????????????
    distanceFromStart(current) = Inf; % remove this node from further consideration
    
    % Compute row, column coordinates of current node
    [i, j] = ind2sub(size(distanceFromStart), current);% ??????????????????????????????????????????????????????????????????
   
   % ********************************************************************* 
    % YOUR CODE BETWEEN THESE LINES OF STARS
    neighbor = [i-1, j ;... 
                i+1, j ;... 
                i, j+1 ;... 
                i, j-1];
                 
    outRangetest = (neighbor(:,1)<1) + (neighbor(:,1)>nrows) +...
   (neighbor(:,2)<1) + (neighbor(:,2)>ncols );
    locate = find(outRangetest>0);  %??????????????????????????????????????? outRangetest ?????? 
    neighbor(locate,:)=[];% =[]????????????????????????
    
    neighborIndex = sub2ind(size(map),neighbor(:,1),neighbor(:,2));%????????????????????????????????????????????????????????????
    for i=1:length(neighborIndex)
        if ((map(neighborIndex(i))~=2) && (map(neighborIndex(i))~=3 && map(neighborIndex(i))~= 5))
        map(neighborIndex(i)) = 4; % ???????????????????????????????????????????????????????????????
       
    % Visit each neighbor of the current node and update the map, distances
    % and parent tables appropriately.
    
         if (distanceFromStart(neighborIndex(i))> min_dist + 1)
            distanceFromStart(neighborIndex(i)) = min_dist+1;  %???????????????????????????
             parent(neighborIndex(i)) = current; % ???????????????????????????
    
    
    
    %*********************************************************************
         end
        end
    end
     
     
end

%% Construct route from start to dest by following the parent links
if (isinf(distanceFromStart(dest_node)))
    route = [];%??????????????????
else
    route = [dest_node];
    
    while (parent(route(1)) ~= 0)
        route = [parent(route(1)), route];% ?????????????????????
    end
    
        % Snippet of code used to visualize the map and the path????????????????????????????????????????????????
    for k = 2:length(route) - 1        
        map(route(k)) = 7;%?????????
        pause(0.1);
        image(1.5, 1.5, map);
        grid on;
        axis image;
    end
end

end
